Uma boa medida para avaliar se o texto foi bem compreendido é a resposta a três questões básicas:
<ol type=I>
<li> Qual é a questão de que o texto está tratando? Ao tentar responder a essa pergunta, o leitor será obrigado a distinguir as questões secundáriasda principal, isto e, aquela em torno da qual gira o texto inteiro. Quando o leitor não sabe dizer do que o texto está tratando, ou sabe apenas de maneira genérica e confusa, é sinal de que ele precisa ser lido com mais atenção ou de que o leitor não tem repertório suficiente para compreender o que está diante de seus olhos.
</li>
<li> Qual é a opinião do autor sobre a questão posta em discussão? Disseminados pelo texto, aparecem vários indicadores da opinião de quem escreve. Por isso, uma leitura competente não terá dificuldade em identificá-la. Não saber dar resposta a essa questão é um sintoma de leitura desatenta e dispersiva.
</li>
<li> Quais são os argumentos utilizados pelo autor para fundamentar a opinião dada? Deve-se entender por argumento todo tipo de recurso usado pelo autor para convencer o leitor de que ele está falando a verdade. Saber reconhecer os argumentos do autor é também um sintoma de leitura bem feita, um sinal claro de que o leitor acompanhou o desenvolvimento das idéias. Na verdade, entender um texto significa acompanhar com atenção o seu percurso argumentatório.
</li>
</ol>
